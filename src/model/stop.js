
class Stop {
    constructor(eva, stationName, stopId, tripCategory, trainNumber, plannedArrival, actualArrival, plannedDeparture, plannedArrivalPlatform, actualArrivalPlatform, plannedDeparturePlatform, actualDeparturePlatform) {
        this.eva = eva
        this.stationName = stationName
        this.stopId = stopId
        this.tripCategory = tripCategory
        this.trainNumber = trainNumber
        this.plannedArrival = plannedArrival
        this.actualArrival = actualArrival
        this.plannedDeparture = plannedDeparture
        this.actualDeparture = actualDeparture
        this.plannedArrivalPlatform = plannedArrivalPlatform
        this.actualArrivalPlatform = actualArrivalPlatform
        this.plannedDeparturePlatform = plannedDeparturePlatform
        this.actualDeparturePlatform = actualDeparturePlatform
    }
}